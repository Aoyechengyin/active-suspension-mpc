% =========================================================================
% Step 1: 面向无区域显式MPC的主动悬架预测模型构建
% =========================================================================
clear; clc;

%% 1. 车辆与执行器物理参数定义 (根据CarSim导出数据更新)
m1 = 450;      % 簧上质量 (kg) - 单角
m2 = 45;       % 簧下质量 (kg) 
k1 = 20000;    % 悬架刚度 (N/m)
k2 = 200000;   % 轮胎刚度 (N/m)
tau = 0.05;    % 执行器时间常数 (s) [cite: 338]
dt = 0.01;     % MPC 离散控制周期 (s) 对应 10ms 
N = 2;         % 预瞄点数 (由于包含了w0, 实际为 w0, w1, w2) 

%% 2. 构建连续时间下的四分之一车辆模型 (QC Model)
% 状态向量 x_qc = [x1; dx1; x1-x2; dx1-dx2; u_a] 
% 输入向量 u  (执行器需求力)
% 扰动输入 w0 (当前车轮处路面高程)

A_qc = [ 0, 1, 0, 0, 0;
         0, 0, -k1/m1, 0, -1/m1;
         0, 0, 0, 1, 0;
         k2/m2, 0, -(k1/m1 + k1/m2 + k2/m2), 0, -(1/m1 + 1/m2);
         0, 0, 0, 0, -1/tau ];

B_qc = [ 0; 0; 0; 0; 1/tau ];

E_qc = [ 0; 0; 0; -k2/m2; 0 ];

C_qc = eye(5);
D_qc = zeros(5,1);

% 构建连续系统状态空间对象
sys_qc_c = ss(A_qc, [B_qc, E_qc], C_qc, [D_qc, zeros(5,1)]);

%% 3. 模型离散化 (Zero-Order Hold)
% 采用ZOH方法将模型离散化，以匹配车载控制器的数字控制特性 
sys_qc_d = c2d(sys_qc_c, dt, 'zoh');

A_qc_d = sys_qc_d.A;
B_qc_d = sys_qc_d.B(:, 1);  % 对应控制输入 u
E_qc_d = sys_qc_d.B(:, 2);  % 对应扰动输入 w0

%% 4. 构建路面预瞄移位寄存器模型
% 预瞄状态 w_hat = [w0; w1; w2]
% 预瞄传感器输入 y_r(k) = w_N(k+1) [cite: 138]

A_r_d = [ 0, 1, 0;
          0, 0, 1;
          0, 0, 0 ];

E_r_d = [ 0; 0; 1 ];

%% 5. 组合扩展状态空间模型
% 增广状态 x_aug = [x1; dx1; x1-x2; dx1-dx2; u_a; w0; w1; w2] 

% 构建组合系统A矩阵 [cite: 144]
A_s_d = [ A_qc_d, E_qc_d, zeros(5, 2);
          zeros(3, 5), A_r_d ];

% 构建组合系统B矩阵 [cite: 144]
B_s_d = [ B_qc_d; 
          zeros(3,1) ];

% 构建组合系统E矩阵 (对应未来路面输入 y_r) [cite: 144]
E_s_d = [ zeros(5,1); 
          E_r_d ];

disp('预测模型构建完成，当前系统矩阵维度：');
fprintf('A_s_d: %d x %d\n', size(A_s_d,1), size(A_s_d,2));
fprintf('B_s_d: %d x %d\n', size(B_s_d,1), size(B_s_d,2));